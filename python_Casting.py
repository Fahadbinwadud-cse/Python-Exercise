# Integers : int,float & string convert to integers
x = int(1)
y = int(1.03)
z = int("7")
print("int,float & string to integer are : ")
print(x,  y,z)
# Floats : int,float & string convert to float
x = float(1)
y = float(9.0)
z = float("3")
print("int,float & string to integer are : ")
print(x,  y,z)

# String : int,float & string convert to string
x = str('2')
y = str('4.7')
z = str("fahad")
print("int,float & string to integer are : ")
print(x,  y, z)